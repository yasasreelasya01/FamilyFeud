    // Store chances state for each domain
    const domainChances = {};
    
    // Create full gameData based on the allDomains list
    allDomains.forEach(domain => {
        if (!gameData[domain]) {
            gameData[domain] = {
                "name": domain,
                "questions": []
            };
            
            // Create sample questions for domains without defined questions
            for (let i = 1; i <= 5; i++) {
                gameData[domain].questions.push({
                    "question": `Sample ${domain} question #${i}`,
                    "answers": [
                        {"text": `Answer 1 for ${domain} Q${i}`, "points": 40 - i},
                        {"text": `Answer 2 for ${domain} Q${i}`, "points": 30 - i},
                        {"text": `Answer 3 for ${domain} Q${i}`, "points": 20 - i}
                    ]
                });
            }
        }
        
        // Initialize chances for each domain
        domainChances[domain] = [false, false, false, false, false]; // false = unused, true = used
    });

    let currentDomain = "";
    let currentQuestion = 0;

    // Initialize the game
    function initGame() {
        const domainsContainer = document.getElementById("domains-container");
        domainsContainer.innerHTML = "";

        // Create domain buttons
        allDomains.forEach(domain => {
            const domainBtn = document.createElement("button");
            domainBtn.className = "domain-btn";
            domainBtn.textContent = domain;
            domainBtn.onclick = () => selectDomain(domain);
            domainsContainer.appendChild(domainBtn);
        });
    }

    // Select a domain
    function selectDomain(domain) {
        currentDomain = domain;
        document.getElementById("domain-title").textContent = domain;
        
        // Create question number buttons
        const questionNumbers = document.querySelector(".question-numbers");
        questionNumbers.innerHTML = "";
        
        // Dynamically create buttons based on actual question count
        const questionCount = gameData[currentDomain].questions.length;
        for (let i = 1; i <= questionCount; i++) {
            const questionBtn = document.createElement("button");
            questionBtn.className = "question-btn";
            questionBtn.textContent = i;
            questionBtn.onclick = () => selectQuestion(i - 1);
            questionNumbers.appendChild(questionBtn);
        }
        
        showQuestionSelector();
    }

    // Select a question
    function selectQuestion(questionIndex) {
        currentQuestion = questionIndex;
        
        // Display the question
        const questionText = document.getElementById("question-text");
        questionText.textContent = gameData[currentDomain].questions[questionIndex].question;
        
        // Create chance circles
        const chancesContainer = document.getElementById("chances-container");
        chancesContainer.innerHTML = "";
        
        for (let i = 0; i < 5; i++) {
            const chanceCircle = document.createElement("div");
            chanceCircle.className = "chance-circle";
            
            // Apply appropriate class based on the stored state
            if (domainChances[currentDomain][i] === "correct") {
                chanceCircle.classList.add("chance-correct");
            } else if (domainChances[currentDomain][i] === "wrong") {
                chanceCircle.classList.add("chance-wrong");
            }
            
            // Add click event for wrong answers
            chanceCircle.onclick = () => {
                if (!domainChances[currentDomain][i]) {
                    markWrong(i);
                }
            };
            
            chancesContainer.appendChild(chanceCircle);
        }
        
        // Create answer elements
        const answersContainer = document.getElementById("answers-container");
        answersContainer.innerHTML = "";
        
        gameData[currentDomain].questions[questionIndex].answers.forEach((answer, index) => {
            const answerElement = document.createElement("div");
            answerElement.className = "answer";
            answerElement.onclick = () => {
                revealAnswer(index);
                markCorrect();
            };
            
            const answerText = document.createElement("div");
            answerText.className = "answer-text";
            answerText.textContent = `${index + 1}. ${answer.text}`;
            
            const answerPoints = document.createElement("div");
            answerPoints.className = "answer-points";
            answerPoints.textContent = answer.points;
            
            answerElement.appendChild(answerText);
            answerElement.appendChild(answerPoints);
            answersContainer.appendChild(answerElement);
        });
        
        showGameArea();
    }

    // Reveal an answer
    function revealAnswer(index) {
        const answerElements = document.querySelectorAll(".answer");
        const answerTextElement = answerElements[index].querySelector(".answer-text");
        answerTextElement.classList.add("reveal");
    }
    
    // Mark a correct answer (finds first unused chance and marks it green)
    function markCorrect() {
        const chanceCircles = document.querySelectorAll(".chance-circle");
        for (let i = 0; i < domainChances[currentDomain].length; i++) {
            if (!domainChances[currentDomain][i]) {
                domainChances[currentDomain][i] = "correct";
                chanceCircles[i].classList.add("chance-correct");
                break;
            }
        }
    }
    
    // Mark a wrong answer (marks specific circle as red)
    function markWrong(index) {
        domainChances[currentDomain][index] = "wrong";
        const chanceCircles = document.querySelectorAll(".chance-circle");
        chanceCircles[index].classList.add("chance-wrong");
    }

    // Show the home screen
    function showHomeScreen() {
        document.getElementById("home-screen").style.display = "block";
        document.getElementById("question-selector").style.display = "none";
        document.getElementById("game-area").style.display = "none";
    }

    // Show the question selector
    function showQuestionSelector() {
        document.getElementById("home-screen").style.display = "none";
        document.getElementById("question-selector").style.display = "block";
        document.getElementById("game-area").style.display = "none";
    }

    // Show the game area
    function showGameArea() {
        document.getElementById("home-screen").style.display = "none";
        document.getElementById("question-selector").style.display = "none";
        document.getElementById("game-area").style.display = "block";
    }

    // Initialize the game when the page loads
    window.onload = initGame;
